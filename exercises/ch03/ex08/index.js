console.log("Number()関数");
console.log("true → " + Number(true));
console.log("1234 → " + Number(1234));
console.log('"text" → ' + Number("text") + "\n");

console.log("Boolean()関数");
console.log("1234 → " + Boolean(1234));
console.log("0 → " + Boolean(0) + "\n");

console.log("String()関数");
console.log("true → " + String(true));
console.log("1234 → " + String(1234) + "\n");

console.log("parseInt()関数");
console.log('"12,742 km：地球の直径" → ' + parseInt("12,742 km：地球の直径"));
console.log('"1.618：黄金比" → ' + parseInt("1.618：黄金比"));
